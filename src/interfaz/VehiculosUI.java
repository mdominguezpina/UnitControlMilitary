    public void listaDeVehiculos(){
        
         //Creamos una lista, y la tabla para listar el personal 
        List <VehiculosClass> listaVehiculos = vehiculo.listaVehiculos();
        tablaModelo = (DefaultTableModel) tablaVehiculos.getModel();
        Object[] object = new Object[5];
        
        tablaModelo.setRowCount(0);
        
        for (int i = 0; i < listaVehiculos.size(); i++) {
            //Recorremos los registros, añadiendolos al Object
            object[0] = listaVehiculos.get(i).getMatriculaVehiculo();
            object[1] = listaVehiculos.get(i).getNombreVehiculo();
            object[2] = listaVehiculos.get(i).getTipo();
            object[3] = listaVehiculos.get(i).getKilometros();
            object[4] = listaVehiculos.get(i).getEstado();
            
            //Añadimos el objeto a la tabla 
            tablaModelo.addRow(object);
            
        }
        //Añadimos la tabla, a la tabla de nuestra interfaz 
        tablaVehiculos.setModel(tablaModelo);
        
    }

