    private void InsertVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertVehiculoActionPerformed
        
        boolean existe = false;
        char letraE = 'E';
        char letraF = 'T';
        /*Verificamos que los campos no esten vacios, e insertamos el registro
        Tambien comprobamos el formato de los campos*/
        if(!"".equals(textMatVeh.getText()) || !"".equals(textNomVeh.getText())
                || !"".equals(textTipVeh.getText()) || !"".equals(textKilVeh.getText())
                || !"".equals(textEstVeh.getText())){
            
            if(textMatVeh.getText().length()!=10 || textMatVeh.getText().charAt(0)!=letraE
                || textMatVeh.getText().charAt(1)!=letraF){
            
            JOptionPane.showMessageDialog(null, "El formato del campo matricula  no es correcto");
            
            }else{
                
                //Recorremos la tabla para verificar que no exista registo igual
                for(int i=0; i<tablaVehiculos.getRowCount(); i++){
                    String MVehiculoTabla = tablaVehiculos.getValueAt(i, 0).toString();
                    if(textMatVeh.getText().equals(MVehiculoTabla)){
                        existe = true; 
                    }
                }
                //Mensaje de error si el campo ya existe
                if(existe==true){
                    JOptionPane.showMessageDialog(null, "Ya existe un registro "
                        + "con el mismo campo numero de matricula");
                }else{
            
                    //añadimos el registro 
                    vehiculoClass.setMatriculaVehiculo(textMatVeh.getText());
                    vehiculoClass.setNombreVehiculo(textNomVeh.getText());
                    vehiculoClass.setTipo(textTipVeh.getText());
                    vehiculoClass.setKilometros(textKilVeh.getText());
                    vehiculoClass.setEstado(textEstVeh.getText());
            
                    vehiculo.insertarVehiculo(vehiculoClass);
                    listaDeVehiculos();
                    limpiarVehiculo();
                    JOptionPane.showMessageDialog(null, "Registro introducido"); 
                }
            }
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Hay campos sin rellenar");
        }
        

    }//GEN-LAST:event_InsertVehiculoActionPerformed

