    public void listaTransmisiones(){
        
        /*Creamos una lista en la cual almacenaremos los datos de nuestra base de datos
        y la añadimos a la tabla de nuestra interfaz para que esten visibles*/
        List <TransmisionesClass> listaTransmisiones = transmisiones.listaTransmisiones();
        tablaModelo = (DefaultTableModel) tablaTransmisiones.getModel();
        Object[] object = new Object[5];
        
        tablaModelo.setRowCount(0);
        
        //Recorremos con un bucle for y añadimos a la tabla
        for (int i = 0; i < listaTransmisiones.size(); i++) {
            
            object[0] = listaTransmisiones.get(i).getNumeroTransmision();
            object[1] = listaTransmisiones.get(i).getNombreTransmision();
            object[2] = listaTransmisiones.get(i).getTipo();
            object[3] = listaTransmisiones.get(i).getEstado();
            object[4] = listaTransmisiones.get(i).getTIM();
            
            tablaModelo.addRow(object);
            
        }
        
        tablaTransmisiones.setModel(tablaModelo);
        
    }

