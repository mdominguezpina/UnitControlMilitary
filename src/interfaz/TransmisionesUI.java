    private void insertTransmisionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertTransmisionesActionPerformed
        
        /*Creamos nos variables para comprobar que esten en el campo numero de transmision
        y que ademas no exista un mismo registro con numero de arma*/
        char letraE = 'E';
        char letraT = 'T';
        boolean existe = false;
        
        /* verificamos que los campos no estan vacios y siguen el formato establecido
        Esto lo haremos mediante varios condicionales*/
        if(!"".equals(textNumTra.getText()) && !"".equals(textNomTra.getText())
                && !"".equals(textTipTra.getText()) && !"".equals(textEstTra.getText())
                && !"".equals(textTimTra.getText())){
                
            if(textTimTra.getText().length()!=12 || Character.isLetter(textTimTra.getText().charAt(11))==false){
            
                JOptionPane.showMessageDialog(null, "El formato del campo TIM no es correcto");
           
            }else if(textNumTra.getText().length()!=10 || textNumTra.getText().charAt(0)!=letraE
                || textNumTra.getText().charAt(1)!=letraT){
            
                JOptionPane.showMessageDialog(null, "El formato del campo numero"
                        + " de transmision no es correcto");
            
            }else{
                
                //Recorremos la tabla para verificar que no exista registo igual
                for(int i=0; i<tablaTransmisiones.getRowCount(); i++){
                    String NtransmisionTabla = tablaTransmisiones.getValueAt(i, 0).toString();
                    if(textNumTra.getText().equals(NtransmisionTabla)){
                        existe = true; 
                    }
                }
                //Mensaje de error si el campo ya existe
                if(existe==true){
                    JOptionPane.showMessageDialog(null, "Ya existe un registro "
                        + "con el mismo campo numero de transmision");
                }else{
                    //Introducimos lo registros 
                    transmisionesClass.setNumeroTransmision(textNumTra.getText());
                    transmisionesClass.setNombreTransmision(textNomTra.getText());
                    transmisionesClass.setTipo(textTipTra.getText());
                    transmisionesClass.setEstado(textEstTra.getText());
                    transmisionesClass.setTIM(textTimTra.getText());
             
                    transmisiones.insertarTransmision(transmisionesClass);
                    listaTransmisiones();
                    limpiarTranmisiones();
                    JOptionPane.showMessageDialog(null, "Registro introducido");
                }
            }
                
        }else if(!"".equals(textNumTra.getText()) && !"".equals(textNomTra.getText())
                && !"".equals(textTipTra.getText()) && !"".equals(textEstTra.getText())){
            
            
            if(textNumTra.getText().length()!=10 || textNumTra.getText().charAt(0)!=letraE
                || textNumTra.getText().charAt(1)!=letraT){
            
                JOptionPane.showMessageDialog(null, "El formato del campo numero"
                        + " de transmision no es correcto");
            
            }else{
                
                //Recorremos la tabla para verificar que no exista registo igual
                for(int i=0; i<tablaTransmisiones.getRowCount(); i++){
                    String NtransmisionTabla = tablaTransmisiones.getValueAt(i, 0).toString();
                    if(textNumTra.getText().equals(NtransmisionTabla)){
                        existe = true; 
                    }
                }
                //Mensaje de error si el campo ya existe
                if(existe==true){
                    JOptionPane.showMessageDialog(null, "Ya existe un registro "
                        + "con el mismo campo numero de transmision");
                }else{
                
                    //Introducimos los registros
                    transmisionesClass.setNumeroTransmision(textNumTra.getText());
                    transmisionesClass.setNombreTransmision(textNomTra.getText());
                    transmisionesClass.setTipo(textTipTra.getText());
                    transmisionesClass.setEstado(textEstTra.getText());
            
                    transmisiones.insertarTransmision(transmisionesClass);
                    listaTransmisiones();
                    limpiarTranmisiones();
                    JOptionPane.showMessageDialog(null, "Registro introducido");
                }
            }
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Aún hay "
                    + "campos sin rellenar");
            
        }
        
    }//GEN-LAST:event_insertTransmisionesActionPerformed

