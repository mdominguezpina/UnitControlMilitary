    private void eliminarTransmisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarTransmisionActionPerformed
    
        //Eliminamos por el numero de transmision, por tanto verificamos que no este vacio
         if(!"".equals(textNumTra.getText())){
            
            int pregunta = JOptionPane.showConfirmDialog(null,
                    "Se eliminará el registro. ¿Estas seguro? ");
            //Hacemos la pregunta de seguridad para borrar el registro
            if(pregunta==0){
                
                //Pregunta de seguridad antes de borrar el registro
                String numTransmision = textNumTra.getText();
                transmisiones.eliminarTransmision(numTransmision);
                listaTransmisiones();
                limpiarTranmisiones();
                JOptionPane.showMessageDialog(null, "Registro eliminado");
            }
           
        }
        
    }//GEN-LAST:event_eliminarTransmisionActionPerformed

    private void modificarTransmisionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarTransmisionesActionPerformed
        
        //Declaramos los variables de tipo char, para verificar que esten en el campo numero transmisiones
        char letraE= 'E';
        char letraT='T';
        
        /*Verificamos tanto que este un registro seleccionado, como que los campos no sean nulos
        y que estos cumplan con el formato especificado*/
        if("".equals(textNumTra.getText())){
            
            JOptionPane.showMessageDialog(null, "No ha "
                    + "seleccionado ningun registro");
            
        }else if (!"".equals(textNomTra.getText()) && !"".equals(textTipTra.getText())
                && !"".equals(textEstTra.getText()) && !"".equals(textTimTra.getText())){
            
            if(textTimTra.getText().length()!=12 || Character.isLetter(textTimTra.getText().charAt(11))==false){
            
           JOptionPane.showMessageDialog(null, "El formato del campo TIM no es correcto");
           
            }else if(textNumTra.getText().length()!=10 || textNumTra.getText().charAt(0)!=letraE
                || textNumTra.getText().charAt(1)!=letraT){
            
            JOptionPane.showMessageDialog(null, "El formato del campo numero"
                    + " de transmision no es correcto");
            
            }else{
            
             //Introducimos lo datos en la tabla 
            transmisionesClass.setNumeroTransmision(textNumTra.getText());
            transmisionesClass.setNombreTransmision(textNomTra.getText());
            transmisionesClass.setTipo(textTipTra.getText());
            transmisionesClass.setEstado(textEstTra.getText());
            transmisionesClass.setTIM(textTimTra.getText());
           
            transmisiones.modificarTransmisiones(transmisionesClass);
                limpiarTranmisiones();
                listaTransmisiones(); 
                JOptionPane.showMessageDialog(null, "Registro modificado");
            }
            
        }else if(!"".equals(textNomTra.getText()) && !"".equals(textTipTra.getText())
                && !"".equals(textEstTra.getText())){
            
            if(textNumTra.getText().length()!=10 || textNumTra.getText().charAt(0)!=letraE
                || textNumTra.getText().charAt(1)!=letraT){
            
            JOptionPane.showMessageDialog(null, "El formato del campo numero"
                    + " de transmision no es correcto");
            
            }else{
            
            //Introducimos los datos en la tabla
            transmisionesClass.setNumeroTransmision(textNumTra.getText());
            transmisionesClass.setNombreTransmision(textNomTra.getText());
            transmisionesClass.setTipo(textTipTra.getText());
            transmisionesClass.setEstado(textEstTra.getText());
            
            transmisiones.modificarTransmisiones(transmisionesClass);
                limpiarTranmisiones();
                listaTransmisiones();
                JOptionPane.showMessageDialog(null, "Registro introducido");
            }    
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Hay campos sin rellenar");
           
        }
        
    }//GEN-LAST:event_modificarTransmisionesActionPerformed

